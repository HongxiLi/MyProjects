    private void backJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backJButtonActionPerformed

        if(request.getSender().getR().equals("Business.Role.CustomerRole")){
        CheckerRecieveCusMsg accounterActionNotice = new CheckerRecieveCusMsg(userProcessContainer, account, organization, business);
        userProcessContainer.add("AccounterActionNotice", accounterActionNotice);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        }
        if(request.getSender().getR().equals("Business.Role.ReplacingDriverRole")){
        CheckerRecieveRdrMsg accounterActionNotice = new CheckerRecieveRdrMsg(userProcessContainer, account, organization, business);
        userProcessContainer.add("AccounterActionNotice", accounterActionNotice);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        }
       
    }//GEN-LAST:event_backJButtonActionPerformed

    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed
        request.setTestResult(resultJTextField.getText());
        request.setStatus("Completed");
        if(request.getSender().getR().equals("Business.Role.CustomerRole")){
            JOptionPane.showMessageDialog(null, "Results have sent to Customer!");
        }
        if(request.getSender().getR().equals("Business.Role.ReplacingDriverRole")){
            JOptionPane.showMessageDialog(null, "Results have sent to ReplacingDriver!");
        }
    }//GEN-LAST:event_submitJButtonActionPerformed

